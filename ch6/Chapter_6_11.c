/*	Data Requirements:
	Relevant Formulas: R = 471.621 * pow(1.063,t); //rumus total pendapatan
	
	Problem Inputs: 
	int revenue = 1000000000; 	//pendapatan
	
	Problem Outputs:
	int t; 						//keuntungan tiap tahun
	double R; 					//jumlah tahun dari tahun 1990
	int year;					//tahun
     Design
     Initial algorithm

     1. buat function predict year untuk mempresdiksi berapa akan mencapai 1 billion $
     menggunakan rumus year = (log10(revenue/471.621)/log10(1.063))+1990+1;
     lalu print hasil nya

     2. Buat function annual revenue menggunakan looping untuk mencari annual revenue

     3. panggil function ke function utama dan tetapkan nilai revenue yaitu 1 billion.

     Implementation
     */

#include <stdio.h>
#include <math.h>

int predict_year(int);
void annual_revenue(int);

int main()
{   //fungsi utama untuk call function dan input nilai revenue
	int revenue = 1000000000;
	printf("Estimated Year at 1 Billions Dollar Revenue > ");
	annual_revenue(predict_year(revenue));
}

int predict_year(int revenue)
{   //fungsi menghitung year pada saat 1 billion $
	int year = (log10(revenue/471.621)/log10(1.063))+1990+1;
	printf("%d\n\n",year);
	return (year);
}

void annual_revenue(int predict_year)
{       //  looping untuk data dari awal sampai 1 billion $ revenue
	double R;
	int t;
	printf("Estimated Year     Revenue\n");
	for(int year = 1990;year<=predict_year;year++){
		t = year - 1990;
		R = 471.621 * pow(1.063,t);
		printf("    %d           $%.3lf\n",year,R);
	}
}


